package com.holomate.core.dto;

public class UserMessage {

    private String message;
    
    

	public UserMessage() {
		super();
	}

	public UserMessage(String message) {
		super();
		this.message = message;
	}

	public String getMessage() {
		return message;
	}

	public void setMessage(String message) {
		this.message = message;
	}

    
}
